char	*ft_strrev(char *str)
{
	int		i;
	int		size;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	size = i;
	i = 0;
	while (i < size / 2)
	{
		c = str[i];
		str[i] = str[size - i - 1];
		str[size - i - 1] = c;
		i++;
	}
	return (str);
}
